package com.theairebellion.zeus.ui.extensions;

/**
 * Defines storage keys for UI-related data.
 * <p>
 * This enum provides keys used for storing UI authentication credentials,
 * responses, and other related data within the framework's storage system.
 * </p>
 *
 * @author Cyborg Code Syndicate
 */
public enum StorageKeysUi {

    /**
     * Key for UI-related storage.
     */
    UI,

    /**
     * Key for storing UI responses.
     */
    RESPONSES,

    /**
     * Key for storing the authenticated username.
     */
    USERNAME,

    /**
     * Key for storing the authenticated password.
     */
    PASSWORD;
}
